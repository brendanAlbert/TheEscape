package edu.orangecoastcollege.escapethecatcher;

/**
 * BoardCodes is a class to encapsulate some constants which represent
 * the three types of places on our game board.
 *
 * The two-dimensional array in GameActivity, gameBoard[][], is comprised
 * of 1s, 2s and 3s, for impassable obstacles, empty passable game board, and the
 * exit, respectively.
 */
public class BoardCodes {
    final static int OBSTACLE = 1;
    final  static int EMPTY = 2;
    final static int EXIT = 3;

}
